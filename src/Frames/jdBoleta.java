        jtxaBoleta.append("ESTADO DEL PRODUCTO\t\t: " + p.getEstado().toUpperCase() + "\n");
        jtxaBoleta.append("CANTIDAD DEL PRODUCTO\t\t: " + p.getCantidad() + "\n");
