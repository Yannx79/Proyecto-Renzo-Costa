    private void establecerValoresAlFrame() {
        jckbExportarAlmacenes.setSelected(true);
        jckbExportarEmpleados.setSelected(true);
        jckbExportarProducto.setSelected(true);
        jckbExportarUsuarios.setSelected(true);
        jrbPorDefectoNombreDocumento.setSelected(true);
        jrbPorDefectoNombreGerente.setSelected(true);
        jrbPorDefectoTipoDeDocumento.setSelected(true);
        jrbTodos.setSelected(true);
        jtxtNombreDelDocumento.setText("TABLAS RENZO COSTA S.A.C.");
        jtxtNombreDelGerente.setText("RENZO COSTA S.A.C.");
    }

    private void limpiarFrame() {
        jtxtNombreDelDocumento.setText("");
        jtxtNombreDelGerente.setText("");
        jrbPorDefectoNombreDocumento.setSelected(false);
        jrbPorDefectoNombreGerente.setSelected(false);
        jrbPorDefectoTipoDeDocumento.setSelected(false);
        jrbPersonalizadoNombreDocumento.setSelected(true);
        jrbPersonalizadoNombreGerente.setSelected(true);
    }

