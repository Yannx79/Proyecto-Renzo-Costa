    private void jbtnInicio1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnInicio1ActionPerformed
        String nombreDocumento = jtxtNombreDelDocumento.getText();
        String nombreGerente = jtxtNombreDelGerente.getText();
        String formato = jcbxTipoDeDocumento.getSelectedItem().toString().toLowerCase();
        boolean imprimirTabla[] = {jckbExportarUsuarios.isSelected(),
            jckbExportarEmpleados.isSelected(), jckbExportarAlmacenes.isSelected(),
            jckbExportarProducto.isSelected()};
        Plantilla p = new Plantilla(nombreGerente, nombreDocumento, formato, imprimirTabla);
        p.buildPlantilla();
        limpiarFrame();
    }//GEN-LAST:event_jbtnInicio1ActionPerformed

