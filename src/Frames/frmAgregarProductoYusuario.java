    private Producto buscarProducto(String codigo) {
        Iterator<Producto> itProducto = listaDeProductos.iterator();
        while (itProducto.hasNext()) {
            Producto p = itProducto.next();
            if (p.getCodigoDeProducto().equalsIgnoreCase(codigo)) {
                productoEncontrado = p;
                break;
            } else {
                productoEncontrado = null;
            }
        }
        return productoEncontrado;
    }

    private Usuario buscarUsuario(String codigo) {
        Iterator<Usuario> itUsario = listaDeUsuario.iterator();
        while (itUsario.hasNext()) {
            Usuario u = itUsario.next();
            if (u.getCodigo().equalsIgnoreCase(codigo)) {
                usuarioEncontrado = u;
                break;
            } else {
                usuarioEncontrado = new Usuario("USUARIO ELIMINADO", "CODIGO ELIMINADO", 0);
            }
        }
        return usuarioEncontrado;
    }

