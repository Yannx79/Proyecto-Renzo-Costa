    private void jtxtCodigoDeUsuarioMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtCodigoDeUsuarioMouseExited
        Iterator<Usuario> itUsuario = listaDeUsuario.iterator();
        String codigoUsuario = jtxtCodigoDeUsuario.getText();
        String nombreUsuario = jtxtNombreDeUsuario.getText();
        long telefono;
        if (jtxtNumeroTelefonico.getText().equalsIgnoreCase("")) {
            telefono = 0;
        } else {
            telefono = Long.parseLong(jtxtNumeroTelefonico.getText());
        }
        int valor;
        while (itUsuario.hasNext()) {
            Usuario u = itUsuario.next();
            if (u.getCodigo().equalsIgnoreCase(codigoUsuario) && u.getNombre().equalsIgnoreCase(nombreUsuario) && u.getTelefono() == telefono) {
            } else {
                if (codigoUsuario.equalsIgnoreCase(u.getCodigo())) {
                    valor = JOptionPane.showConfirmDialog(this, "ESE CODIGO ESTA REGISTRADO EN LA BASE DE DATOS\n¿DESEA USAR EL CLIENTE YA REGISTRADO?", "USUARIO REGISTRADO", JOptionPane.INFORMATION_MESSAGE);
                    if (valor == 0) {
                        jtxtNombreDeUsuario.setText(u.getNombre());
                        jtxtNumeroTelefonico.setText(String.valueOf(u.getTelefono()));
                    }
                    if (valor == 1 || valor == 2) {
                        jtxtNombreDeUsuario.setText("");
                        jtxtNumeroTelefonico.setText("");
                        jtxtCodigoDeUsuario.setText("");
                    }
                }
            }
        }
    }//GEN-LAST:event_jtxtCodigoDeUsuarioMouseExited

