    private void jbtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnConsultarActionPerformed
        String codigoProducto = jtxtCodigoDeProducto.getText();
        if (codigoProducto.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Ingrese un codigo por favor");
        } else {
            try {
                productoEncontrado = buscarProducto(codigoProducto);/*REDUNDANCIA PUEDE SER VOID*/
                usuarioEncontrado = buscarUsuario(productoEncontrado.getCodigoDeUsuario());
            } catch (NullPointerException nullPointerException) {
                nullPointerException.getMessage();
                nullPointerException.getLocalizedMessage();
            }
            if (productoEncontrado != null) {
                jcbxCategoria.setSelectedItem(productoEncontrado.getCategoria());
                jtxtNombreDeProducto.setText(productoEncontrado.getNombreDeProducto());
                jtxtPrecioDelProducto.setText(String.valueOf(productoEncontrado.getPrecioUnitario()));
                jcbxAlmacen.setSelectedItem(productoEncontrado.getAlmacen());
                jtxtDestinoDelProducto.setText(productoEncontrado.getDestino());
                jtxtCantidaDeProducto.setText(String.valueOf(productoEncontrado.getCantidad()));
                jcbxEstado.setSelectedItem(productoEncontrado.getEstado());
                SimpleDateFormat df = new SimpleDateFormat("dd/MM/YYYY");
                try {
                    jcFechaDeEntregaDeProducto.setDate(df.parse(productoEncontrado.getFechaProgramadaDeEntrega()));
                    jcFechaDeRegistroDeProducto.setDate(df.parse(productoEncontrado.getFechaDeRegistro()));
                } catch (Exception e) {
                    e.getMessage();
                    e.getLocalizedMessage();
                }
                jtxtNumeroTelefonico.setText(String.valueOf(usuarioEncontrado.getTelefono()));
                jtxtCodigoDeUsuario.setText(usuarioEncontrado.getCodigo());
                jtxtNombreDeUsuario.setText(usuarioEncontrado.getNombre());
                desabilitarTextField();
                habilitarBoton();
            } else {
                JOptionPane.showMessageDialog(this, "El codigo: " + codigoProducto + ", no esta en la lista...", "NO SE ENCUENTRA EN LA LISTA", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbtnConsultarActionPerformed

