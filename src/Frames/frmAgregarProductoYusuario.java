    private void jbtnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAgregarProductoActionPerformed
        SimpleDateFormat sdfFechas = new SimpleDateFormat("dd/MM/YYYY");
        String codigoDeProducto = jtxtCodigoDeProducto.getText();
        String estado = String.valueOf(jcbxEstado.getSelectedItem()); //En Almacen, Partiendo, En Camino, Entregado
        int cantidad = Integer.parseInt(jtxtCantidaDeProducto.getText());
        String categoria = String.valueOf(jcbxCategoria.getSelectedItem()); // Calzado, Casaca, Billeteras, Carteras, Mochilas, Correas, Accesorios
        String nombreDeProducto = jtxtNombreDeProducto.getText();
        double precioUnitario = Double.parseDouble(jtxtPrecioDelProducto.getText());
        String almacen = String.valueOf(jcbxAlmacen.getSelectedItem()); //Partida = {Almacen Centro, Almacen Norte, Almacen Oeste, Almacen Sur, Almacen Este}
        String destino = jtxtDestinoDelProducto.getText();
        String fechaDeRegistro = sdfFechas.format(jcFechaDeRegistroDeProducto.getDate());
        String fechaProgramadaDeEntrega = sdfFechas.format(jcFechaDeEntregaDeProducto.getDate());
        String codigoDeUsuario = jtxtCodigoDeUsuario.getText();
        Producto p = new Producto(codigoDeProducto, estado, cantidad, categoria, nombreDeProducto, precioUnitario, almacen, destino, fechaDeRegistro, fechaProgramadaDeEntrega, codigoDeUsuario);
        listaDeProductos.addLast(p);
        cd.sobreEscribirEnArchivoProductos(listaDeProductos);
        cd.sobreEscribirEnTablaDeProductosInicioFin(dtmTablaDeProductos);
        limpiarCasillasProducto();
        jdBoleta dialogBoleta = new jdBoleta(this, true);
        dialogBoleta.setVisible(true);
    }//GEN-LAST:event_jbtnAgregarProductoActionPerformed

