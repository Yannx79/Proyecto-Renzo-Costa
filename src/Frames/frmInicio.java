    private void agregarProductoBD() {
        Conexion conexion = new Conexion();
        Connection connection = conexion.getConnection();
        Iterator<Producto> itProducto = listaProducto.iterator();
        int contador = 0;
//        DecimalFormat df = new DecimalFormat("####.00");
        try {

            PreparedStatement ps = connection.prepareStatement(
                    "INSERT INTO `productos`(`id`,`CODIGO DE PRODUCTO`, "
                    + "`ESTADO`, `CANTIDAD`, `CATEGORIA`, `NOMBRE`, "
                    + "`PRECIO UNITARIO`, `ALMACEN`, `DESTINO`, "
                    + "`FECHA DE REGISTRO`, `FECHA DE ENTREGA`, "
                    + "`CODIGO DE USUARIO`, `PRECIO TOTAL`) "
                    + "VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?)"
            );
            while (itProducto.hasNext()) {
                contador++;
                Producto p = itProducto.next();
                ps.setInt(1, contador);
                ps.setString(2, p.getCodigoDeProducto());
                ps.setString(3, p.getEstado());
                ps.setInt(4, p.getCantidad());
                ps.setString(5, p.getCategoria());
                ps.setString(6, p.getNombreDeProducto());
                ps.setDouble(7, p.getPrecioUnitario());
                ps.setString(8, p.getAlmacen());
                ps.setString(9, p.getDestino());
                ps.setString(10, p.getFechaDeRegistro());
                ps.setString(11, p.getFechaProgramadaDeEntrega());
                ps.setString(12, p.getCodigoDeUsuario());
                ps.setDouble(13, p.calcularPrecioDeUnTipoDeProducto());
                ps.executeUpdate();
            }
            connection.close();
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }

    private void eliminarTodasLasTablas() {
        eliminarTablas("usuarios");
        eliminarTablas("productos");
        eliminarTablas("empleado");
    }

    private void eliminarTablas(String nombreTabla) {
        Conexion conexion = new Conexion();
        Connection connection = conexion.getConnection();
        try {
            PreparedStatement ps = connection.prepareStatement(
                    "delete from " + nombreTabla
            );
            ps.executeUpdate();
            connection.close();
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }

