    private void actualizarTodasLasTablas(){
        eliminarTodasLasTablas();
        agregarUsariosBD();
        agregarEmpleadoBD();
        agregarProductoBD();
    }
    
    private void agregarUsariosBD() {
        Iterator<Usuario> itUsuario = listaUsuario.iterator();
        try {

            Conexion conexion = new Conexion();
            Connection connection = conexion.getConnection();
            PreparedStatement ps = connection.prepareStatement(
                    "insert into usuarios (codigo, nombre, telefono) values (?,?,?)"
            );
            int resultado = 0;
            while (itUsuario.hasNext()) {
                Usuario u = itUsuario.next();
                ps.setString(1, u.getCodigo());
                ps.setString(2, u.getNombre());
                ps.setLong(3, u.getTelefono());
                resultado = ps.executeUpdate();
            }
            if (resultado > 0) {
                System.out.println("SE EJECUTO CORRECTO");
            } else {
                System.out.println("NO SE EJECUTO CORRECTO");
            }
            connection.close();
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }

    }

